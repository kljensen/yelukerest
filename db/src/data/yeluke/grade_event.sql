CREATE OR REPLACE FUNCTION prevent_grade_event_mutation()
RETURNS TRIGGER AS $$
BEGIN
    RAISE EXCEPTION 'grade event history is append-only';
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS tg_assignment_grade_event_append_only ON assignment_grade_event;
CREATE TRIGGER tg_assignment_grade_event_append_only
    BEFORE UPDATE OR DELETE
    ON assignment_grade_event
    FOR EACH ROW
EXECUTE FUNCTION prevent_grade_event_mutation();

