CREATE TABLE IF NOT EXISTS assignment_grade_event (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_type TEXT NOT NULL
        CHECK (event_type = ANY (ARRAY['recorded', 'corrected', 'voided'])),
    operation TEXT NOT NULL
        CHECK (operation = ANY (ARRAY['insert', 'update', 'delete'])),
    assignment_submission_id INT NOT NULL,
    assignment_slug TEXT NOT NULL
        CHECK (char_length(assignment_slug) < 100),
    points_possible SMALLINT NOT NULL
        CHECK (points_possible >= 0),
    points REAL NOT NULL,
    description TEXT,
    grade_created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    grade_updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT current_timestamp,
    created_by_user_id INT,
    source TEXT NOT NULL DEFAULT 'data.assignment_grade',
    reason TEXT,
    import_id TEXT,
    CONSTRAINT assignment_grade_event_points_in_range
        CHECK (points >= 0 AND points <= points_possible)
);

DROP INDEX IF EXISTS idx_assignment_grade_event_natural_key;
CREATE INDEX idx_assignment_grade_event_natural_key
    ON assignment_grade_event (assignment_submission_id, created_at, id);

DROP INDEX IF EXISTS idx_assignment_grade_event_actor_fk;
CREATE INDEX idx_assignment_grade_event_actor_fk
    ON assignment_grade_event (created_by_user_id);

