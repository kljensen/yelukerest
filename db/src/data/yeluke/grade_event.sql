CREATE OR REPLACE FUNCTION record_assignment_grade_event()
RETURNS TRIGGER AS $$
DECLARE
    grade_row data.assignment_grade%ROWTYPE;
    event_kind TEXT;
BEGIN
    IF (TG_OP = 'DELETE') THEN
        grade_row := OLD;
        event_kind := 'voided';
    ELSE
        grade_row := NEW;
        IF (TG_OP = 'INSERT') THEN
            event_kind := 'recorded';
        ELSE
            event_kind := 'corrected';
        END IF;
    END IF;

    INSERT INTO data.assignment_grade_event (
        event_type,
        operation,
        assignment_submission_id,
        assignment_slug,
        points_possible,
        points,
        description,
        grade_created_at,
        grade_updated_at,
        created_by_user_id
    )
    VALUES (
        event_kind,
        lower(TG_OP),
        grade_row.assignment_submission_id,
        grade_row.assignment_slug,
        grade_row.points_possible,
        grade_row.points,
        grade_row.description,
        grade_row.created_at,
        grade_row.updated_at,
        request.user_id()
    );

    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = data, pg_temp;

DROP TRIGGER IF EXISTS tg_assignment_grade_event_history ON assignment_grade;
CREATE TRIGGER tg_assignment_grade_event_history
    AFTER INSERT OR UPDATE OR DELETE
    ON assignment_grade
    FOR EACH ROW
EXECUTE FUNCTION record_assignment_grade_event();

